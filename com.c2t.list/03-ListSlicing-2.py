fruits = ['orange', 'apple', 'pear', 'banana']
print('fruits=',fruits)
print('fruits[0]=',fruits[0])
print('fruits[0:2]=',fruits[0:2])
print('fruits[0:len(fruits)]=',fruits[0:len(fruits)])
print('fruits[0:]=',fruits[0:])
print('fruits[2:]=',fruits[2:])
print('fruits[0:-1]=',fruits[0:-1])
print('fruits[:4]=',fruits[:4])
print('fruits[:]=',fruits[:])

