import numpy as np

arr_1d =  np.arange(0,11)
print('arr_1d =',arr_1d)

print('arr_1d[0] =',arr_1d[0]) # Accessing 0th element

print('arr_1d[1:5] =',arr_1d[1:5]) # Slicing array. Accessing element from 1 to 5, excluding 5

print('arr_1d[:5] =',arr_1d[:5]) # Slicing element from beginning to 5, excluding 5

print('arr_1d[5:] =',arr_1d[5:]) # Slicing elements from 5 to end of array