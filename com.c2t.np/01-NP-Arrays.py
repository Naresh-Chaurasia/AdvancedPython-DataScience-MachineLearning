import numpy as np

# 1D
array_1d = [1,2,3,4,5] # Creating single dimensional array
print('array_1d:',array_1d)

np_array_1d = np.array(array_1d) # Creating NumPy single dimensional array
print('np_array_1d:',np_array_1d)
print('type(np_array_1d):',type(np_array_1d))
print('')

# 2D
array_2d = [[1,2,3,4,5],[10,20,30,40,50]] # Creating two dimensional array
print('array_2d:',array_2d)
np_array_2d = np.array(array_2d) # Creating NumPy two dimensional array
print('np_array_2d:',np_array_2d)
print('type(np_array_2d):',type(np_array_2d))

