import numpy as np

np_arr = np.arange(0,11)
print('np_arr =',np_arr)

#Array with Array Operations
print('np_arr + np_arr =',np_arr+np_arr)
print('np_arr - np_arr =',np_arr-np_arr)
print('np_arr * np_arr =',np_arr*np_arr)
print('np_arr ** np_arr =',np_arr**np_arr)

#Scalar operations
print('---------------Scalar Operations---------------')
print('np_arr + 100 =',np_arr + 100)
print('np_arr / np_arr =',np_arr / np_arr)
print('1 / np_arr =',1 / np_arr)
